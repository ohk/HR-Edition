    private void btn_projeUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_projeUpdateActionPerformed
        if (projectIDs > 0) {
            Projects p = null;
            try {
                p = db.getProject(projectIDs);
                p.setProjectName(txt_ProjectName.getText());
                p.setManagerMIN(Integer.parseInt(txt_minManager.getText()));
                p.setManagerMAX(Integer.parseInt(txt_maxManager.getText()));
                p.setDesignerMIN(Integer.parseInt(txt_minDesigner.getText()));
                p.setDesignerMAX(Integer.parseInt(txt_maxDesigner.getText()));
                p.setDeveloperMIN(Integer.parseInt(txt_minProgrammer.getText()));
                p.setDeveloperMAX(Integer.parseInt(txt_maxProgrammer.getText()));
                p.setAnalystMIN(Integer.parseInt(txt_minAnalist.getText()));
                p.setAnalystMAX(Integer.parseInt(txt_maxAnalist.getText()));
                p.setTesterMIN(Integer.parseInt(txt_minTester.getText()));
                p.setTesterMAX(Integer.parseInt(txt_maxTester.getText()));
                db.updateProject(p);
                JOptionPane.showMessageDialog(null,
                        "Project Updated",
                        "INFO",
                        JOptionPane.INFORMATION_MESSAGE);
                clearFields();
            projectToTable();
            } catch (SQLException ex) {
                Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btn_projeUpdateActionPerformed

