    private void personelToTable() throws SQLException {
        DefaultTableModel model = (DefaultTableModel) personelTable.getModel();
        model.setRowCount(0);
        Object rowData[] = new Object[13];
        ArrayList<Employee> employees = db.getEmployees();

        for (int i = 0; i < employees.size(); i++) {
            rowData[0] = employees.get(i).getTC();
            rowData[1] = employees.get(i).getProjectId();
            rowData[2] = employees.get(i).getName();
            rowData[3] = employees.get(i).getSurname();
            rowData[4] = employees.get(i).getPhone();
            rowData[5] = employees.get(i).getType();
            rowData[6] = employees.get(i).getAddress();
            rowData[7] = employees.get(i).getSalary();
            rowData[8] = employees.get(i).getSalaryType();
            rowData[9] = employees.get(i).getBegin();
            rowData[10] = employees.get(i).getEnd();
            rowData[11] = employees.get(i).getIsWorking();
            rowData[12] = employees.get(i).getCompensation();

            model.addRow(rowData);
        }

    }

    private void projectToTable() throws SQLException {
        DefaultTableModel model = (DefaultTableModel) projectTable.getModel();
        model.setRowCount(0);
        Object rowData[] = new Object[20];
        ArrayList<Projects> p = db.getProjects();

        for (int i = 0; i < p.size(); i++) {
            rowData[0] = p.get(i).getProjectID();
            rowData[1] = p.get(i).getProjectName();
            rowData[2] = p.get(i).getManagerMIN();
            rowData[3] = p.get(i).getManagerMAX();
            rowData[4] = p.get(i).getManagerCNT();
            rowData[5] = p.get(i).getDesignerMIN();
            rowData[6] = p.get(i).getDesignerMAX();
            rowData[7] = p.get(i).getDesignerCNT();
            rowData[8] = p.get(i).getDeveloperMIN();
            rowData[9] = p.get(i).getDeveloperMAX();
            rowData[10] = p.get(i).getDeveloperCNT();
            rowData[11] = p.get(i).getAnalystMIN();
            rowData[12] = p.get(i).getAnalystMAX();
            rowData[13] = p.get(i).getAnalystCNT();
            rowData[14] = p.get(i).getTesterMIN();
            rowData[15] = p.get(i).getTesterMAX();
            rowData[16] = p.get(i).getTesterCNT();
            rowData[17] = p.get(i).getProjectStatus();
            rowData[18] = p.get(i).getBegin();
            rowData[19] = p.get(i).getEnd();

            model.addRow(rowData);
        }

    }

